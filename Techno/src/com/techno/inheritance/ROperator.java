package com.techno.inheritance;

public class ROperator {

	public static void main(String[] args) {
		int x=5;
		int y=6;
		System.out.println((x++>y)||(x++<y));
		System.out.println(x);
		System.out.println(y);
		System.out.println("********");
		System.out.println(5|6);
		System.out.println(5&6);
		System.out.println((++x>=y)|(++x<=y));
		System.out.println(x);
		System.out.println(y);
		System.out.println(4|10);

	}

}
